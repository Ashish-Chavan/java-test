/**
 * Java 15 Feature: Hidden Classes (JEP 371)
 * Hidden classes are classes that cannot be used directly by the bytecode
 * of other classes. They are intended for use by frameworks that generate
 * classes at runtime and use them indirectly via reflection.
 *
 * Key properties:
 * - Not discoverable by class loaders
 * - Cannot be referenced by other classes
 * - Can be unloaded independently of their defining class loader
 * - Created via Lookup.defineHiddenClass()
 */
import java.lang.invoke.MethodHandle;
import java.lang.invoke.MethodHandles;
import java.lang.invoke.MethodType;

public class HiddenClasses {

    // Bytecode for a simple class that implements Runnable:
    // public class GeneratedRunner implements Runnable {
    //     public void run() { System.out.println("Hidden class running!"); }
    // }
    // (In real use, this would be dynamically generated bytecode)
    private static final byte[] HIDDEN_CLASS_BYTES = generateSimpleBytecode();

    public static void main(String[] args) throws Throwable {

        MethodHandles.Lookup lookup = MethodHandles.lookup();

        // Define a hidden class from bytecode
        MethodHandles.Lookup hiddenLookup = lookup.defineHiddenClass(
            HIDDEN_CLASS_BYTES,
            true,  // initialize immediately
            MethodHandles.Lookup.ClassOption.NESTMATE  // make it a nestmate
        );

        Class<?> hiddenClass = hiddenLookup.lookupClass();

        // Hidden classes have a special name with a slash suffix
        System.out.println("Hidden class name: " + hiddenClass.getName());
        System.out.println("Is hidden: " + hiddenClass.isHidden());

        // Cannot be found by Class.forName()
        try {
            Class.forName(hiddenClass.getName());
            System.out.println("Found by forName (unexpected)");
        } catch (ClassNotFoundException e) {
            System.out.println("Not findable by Class.forName() - as expected");
        }

        // Invoke via MethodHandle
        MethodHandle constructor = hiddenLookup.findConstructor(
            hiddenClass,
            MethodType.methodType(void.class)
        );
        Runnable instance = (Runnable) constructor.invoke();
        instance.run();

        // Demonstrating use case: framework-style dynamic class generation
        System.out.println("\n--- Framework Simulation ---");
        DynamicProxy proxy = new DynamicProxy();
        proxy.invoke("processData", "sample input");
    }

    // Simulated framework that would use hidden classes internally
    static class DynamicProxy {
        void invoke(String methodName, Object arg) {
            // In real frameworks (like lambdas, proxies), hidden classes
            // are generated here dynamically
            System.out.println("Dynamic dispatch: " + methodName + "(" + arg + ")");
            System.out.println("(In JDK internals, lambda metafactory uses hidden classes)");
        }
    }

    /**
     * Generates minimal valid bytecode for a class that implements Runnable.
     * This is a placeholder — real frameworks use ASM/ByteBuddy to generate this.
     */
    private static byte[] generateSimpleBytecode() {
        // Minimal class file bytes for:
        // public class HiddenRunner implements Runnable {
        //     public void run() {}
        // }
        return new byte[]{
            (byte)0xCA, (byte)0xFE, (byte)0xBA, (byte)0xBE, // magic
            0x00, 0x00, 0x00, 0x3D,                          // version 61 = Java 17
            0x00, 0x0D,                                       // constant pool count = 13
            0x0A, 0x00, 0x03, 0x00, 0x09,                    // #1 Methodref Object.<init>
            0x07, 0x00, 0x0A,                                 // #2 Class HiddenRunner
            0x07, 0x00, 0x0B,                                 // #3 Class java/lang/Object
            0x07, 0x00, 0x0C,                                 // #4 Class java/lang/Runnable
            0x01, 0x00, 0x06, '<', 'i', 'n', 'i', 't', '>',  // #5 "<init>"
            0x01, 0x00, 0x03, '(', ')', 'V',                  // #6 "()V"
            0x01, 0x00, 0x04, 'C', 'o', 'd', 'e',            // #7 "Code"
            0x01, 0x00, 0x03, 'r', 'u', 'n',                  // #8 "run"
            0x01, 0x00, 0x0C, 'N', 'a', 'm', 'e', 'O', 'f', 'H', 'i', 'd', 'd', 'e', 'n',
            0x01, 0x00, 0x10, 'j', 'a', 'v', 'a', '/', 'l', 'a', 'n', 'g', '/', 'O', 'b', 'j', 'e', 'c', 't',
            0x01, 0x00, 0x12, 'j', 'a', 'v', 'a', '/', 'l', 'a', 'n', 'g', '/', 'R', 'u', 'n', 'n', 'a', 'b', 'l', 'e',
            0x00, 0x21,             // access_flags: public
            0x00, 0x02,             // this_class: #2
            0x00, 0x03,             // super_class: #3
            0x00, 0x01, 0x00, 0x04, // interfaces_count=1, interface[0]=#4
            0x00, 0x00,             // fields_count = 0
            0x00, 0x02,             // methods_count = 2
            // <init> method
            0x00, 0x01, 0x00, 0x05, 0x00, 0x06, 0x00, 0x01,
            0x00, 0x07, 0x00, 0x00, 0x00, 0x11, 0x00, 0x01,
            0x00, 0x01, 0x00, 0x00, 0x00, 0x05, 0x2A, (byte)0xB7, 0x00, 0x01, (byte)0xB1,
            0x00, 0x00, 0x00, 0x00,
            // run method
            0x00, 0x01, 0x00, 0x08, 0x00, 0x06, 0x00, 0x01,
            0x00, 0x07, 0x00, 0x00, 0x00, 0x0D, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x01, (byte)0xB1,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00  // attributes_count = 0
        };
    }
}
